// ═══════════════════════════════════════════════════════════════════════════
//  CUSTOM KEYCODE HANDLER
// ═══════════════════════════════════════════════════════════════════════════
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    uint8_t row = record->event.key.row;
    uint8_t col = record->event.key.col;

    // ── Layer toggle (capslock style) ──────────────────────────────────
    if (keycode == KC_LAYER_TOG) {
        if (record->event.pressed) {
            if (IS_LAYER_ON(_GIF)) {
                layer_off(_GIF);
                active_gif     = -1;   // stop all GIFs when leaving layer
                bongo_other_pressed = false;
            } else {
                layer_on(_GIF);
            }
        }
        return false;
    }

    // ── GIF layer key handling ─────────────────────────────────────────
    if (IS_LAYER_ON(_GIF)) {
        if (record->event.pressed) {
            // Determine which GIF to trigger
            int8_t new_gif = -1;
            if      (row == 0 && col == 0) new_gif = 0;  // walking anim
            else if (row == 0 && col == 1) new_gif = 1;  // bongo cat
            else if (row == 1 && col == 0) new_gif = 2;  // text scroll
            else if (row == 1 && col == 1) new_gif = 3;  // anydesk static

            if (new_gif >= 0) {
                if (active_gif == new_gif) {
                    // Same button pressed again → stop
                    active_gif = -1;
                    bongo_other_pressed = false;
                } else {
                    active_gif = new_gif;
                    frame_idx  = 0;
                    last_frame_t = timer_read32();
                    // Track bongo "other" state
                    bongo_other_pressed = (new_gif != 1 && active_gif == 1);
                }
                oled_clear();
            }
        } else {
            // Key released
            if (row == 0 && col == 1) {
                // R0C1 released → bongo back to idle frame if active
            }
            // If any non-R0C1 key released while bongo is active
            if (active_gif == 1 && !(row == 0 && col == 1)) {
                bongo_other_pressed = false;
            }
        }
    }

    // Track bongo "other button pressed" while bongo gif is active
    if (active_gif == 1 && IS_LAYER_ON(_GIF)) {
        if (record->event.pressed && !(row == 0 && col == 1)) {
            bongo_other_pressed = true;
        }
        if (!record->event.pressed && !(row == 0 && col == 1)) {
            bongo_other_pressed = false;
        }
    }

    return true;
}


